package aula20171107;

public class Person {

	private String number;
	private String name;
	private ContactBook contactBook;

	public Person(String number, String name) {
		this.number = number;
		this.name = name;
		this.contactBook = new ContactBook();
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((number == null) ? 0 : number.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Person other = (Person) obj;
		if (number == null) {
			if (other.number != null)
				return false;
		} else if (!number.equals(other.number))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Contact [number=" + number + ", name=" + name + "]";
	}

	public ContactBook getContactBook() {
		return contactBook;
	}
}
